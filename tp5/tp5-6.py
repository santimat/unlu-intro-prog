day_number = int(input("Ingrese un numero del 1 al 7"))

if day_number == 1:
  print("Lunes")
elif day_number == 2:
  print("Martes")
elif day_number == 3:
  print("Miercoles")
elif day_number == 4:
  print("Jueves")
elif day_number == 5:
  print("Viernes")
elif day_number == 6:
  print("Sabado")
elif day_number == 7:
  print("Domingo")
else: 
  print("Numero invalido")